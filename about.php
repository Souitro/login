<h2><b style="color:gray">What is an 'About Us' page?</b></h2>
<p>An <i><b style="color:blue">'About Us'</b></i> page is a spot for your founding story,<br>
a place to show off your business wins, and a sales page that answers<br>
the most pressing question new customers have about your business.<br>

What an 'About Us' page should be is a goal-oriented sales page, one that<br>
focuses on highlighting the biggest selling points of your story and brand<br>
at the top of the page, making a strong first impression on curious customers<br>
In Shopify’s customer trust research, we found that shoppers navigate to an <br>
About Us page to learn more about the brand and the people behind the products.</p><p>
Your 'About Page' should address those two curiosities shoppers have to help with <br>
decision making. Shoppers are also interested in a company’s mission. They’ll use the<br>
About Us page to determine if they share core values with the business and to decide if<br>
they want to shop with the business or not. In contrast to a landing page, your About Us <br>
page is the ideal place to accommodate a number of objectives:</p>
<p>When in doubt, start with a question. That’s exactly what Magic Spoon—a brand that creates<br>
healthy cereals for grownups—does with the introductory paragraph on its About Us page.<br>

Want to take it to a level higher? Start your About page with a question that pulls on your <br>
audience’s emotions. The makers of Magic Spoon pose an effective first question that makes you think:<br> 

“Remember the carefree days of childhood, when you didn’t feel guilty about eating mountains of <br>
cereal with questionable nutrition?”</p><p>Additionally, don’t be afraid to use bright, fun colors on <br>
your About Us page (assuming they’re part of your brand identity) to make your brand more memorable.<br>
 Now, where Magic Spoon really shines is its comparison chart. It doesn’t hold back on giving you a <br>
 clear visual of how its cereal compares to the competition. Plus, Magic Spoon’s copy is relatable to<br>
 its audience. Why sound like a stiff corporate welcome packet when you don’t have to?<br>
</p></body>